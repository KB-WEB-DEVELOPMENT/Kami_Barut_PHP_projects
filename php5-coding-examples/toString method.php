<?php

// semi-automatic object to string conversion with echo and print (automatic starting with 5.2)

class Object {
	
	function __toString() {
		
		return 'Object as string<br/>';
	}
}

$o = new Object;

echo $o; // output: Object as string

$str = (string) $o;

print_r($str); // output: Object as string

?>
